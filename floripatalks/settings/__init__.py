# Settings package
#
# Note: This package does not import any settings by default.
# Settings must be explicitly loaded via DJANGO_SETTINGS_MODULE:
#   - floripatalks.settings.development (for local development)
#   - floripatalks.settings.production (for production)
#   - floripatalks.settings.test (for tests)
#
# This prevents development-only dependencies (like python-dotenv) from
# being required in production when importing the settings package.
